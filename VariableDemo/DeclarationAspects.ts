// 2.Declaration/value Assignment

//Example 1: var can declared without initalization

var x;             //Declaration
console.log(x);    //it gives an result undefined

x=30;             //Intialization
console.log(x);

//Example 2: let also can declared without initalization

let y;
console.log(y);   //it gives an result undefined

y=30;
console.log(y);


//Example 3: const must be intialized at the time of declaration

//const z;         //it given an error and it is incorrect
const z=50;      //correct
console.log(z);
